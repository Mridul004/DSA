            if ((row + col) % 2 == 0) {
                if (col == n - 1) row++;
            result[i] = mat[row][col];
                else if (row == 0) col++;
                else { row--; col++; }
            } else {
                if (row == m - 1) col++;
                else if (col == 0) row++;
                else { row++; col--; }
            }
        }
        return result;
    }